"""
Synthesize dataset variants from preprocessed Pepper&Carrot renders.

Source: data/preprocessing/renders/{episode}/initial/ + cleaned/
Output: data/dataset/{episode}/{variant}/

Variants per page — pairs sort adjacent in any file browser:

  black/              — artwork on solid black background
  framed/             — initial render + 1px black panel outline
  framed_cleaned/     — cleaned artwork + 1px outline (transparent bg)
  framed_jpeg/        — initial render + 1px frame + JPEG compression
  framed_jpeg_cleaned/— cleaned artwork + 1px frame + JPEG compression
  gradient_border/    — borders filled black→white across full page height
  gradient_border_inv/— borders filled white→black across full page height
  jpeg/               — initial render with heavy JPEG compression
  jpeg_cleaned/       — cleaned artwork + JPEG artifacts on RGB, alpha kept

Overlay variants (generated by synthesize_overlays.py):
  sfx_overlay/        — Korean SFX on initial render
  sfx_overlay_cleaned/— same SFX positions on cleaned artwork (RGBA)
  bubble_overlay/     — speech bubbles on initial render
  bubble_overlay_cleaned/ — same bubble positions on cleaned artwork (RGBA)
"""

import argparse
import io
from pathlib import Path

import numpy as np
from PIL import Image, ImageFilter
try:
    from tqdm import tqdm
except ImportError:
    class tqdm:
        def __init__(self, it, desc="", **kw):
            self._it = list(it); self._desc = desc; self._n = 0
            print(f"{desc}: 0/{len(self._it)}", end="\r", flush=True)
        def __iter__(self):
            for x in self._it:
                yield x
                self._n += 1
                print(f"{self._desc}: {self._n}/{len(self._it)}", end="\r", flush=True)
            print()
        def set_postfix(self, **kw): pass
        @staticmethod
        def write(msg): print(msg)

RENDERS_DIR = Path("data/preprocessing/renders")
DATASET_DIR = Path("data/dataset")

JPEG_QUALITY = 15


def panel_edge(transparent: Image.Image) -> np.ndarray:
    alpha = np.array(transparent.getchannel("A"))
    border = Image.fromarray((alpha == 0).astype(np.uint8) * 255)
    dilated = np.array(border.filter(ImageFilter.MaxFilter(3))) > 0
    return dilated & (alpha > 0)


def make_black_variant(transparent: Image.Image) -> Image.Image:
    bg = Image.new("RGBA", transparent.size, (0, 0, 0, 255))
    bg.alpha_composite(transparent)
    return bg


def make_jpeg_variant(merged: Image.Image) -> Image.Image:
    buf = io.BytesIO()
    merged.convert("RGB").save(buf, format="JPEG", quality=JPEG_QUALITY)
    buf.seek(0)
    return Image.open(buf).copy().convert("RGBA")


def make_framed_variant(transparent: Image.Image) -> Image.Image:
    bg = Image.new("RGBA", transparent.size, (255, 255, 255, 255))
    bg.alpha_composite(transparent)
    arr = np.array(bg)
    arr[panel_edge(transparent)] = [0, 0, 0, 255]
    return Image.fromarray(arr)


def make_transparent_framed_variant(transparent: Image.Image) -> Image.Image:
    arr = np.array(transparent.copy())
    arr[panel_edge(transparent)] = [0, 0, 0, 255]
    return Image.fromarray(arr)


def make_transparent_jpeg_variant(transparent: Image.Image) -> Image.Image:
    alpha = transparent.getchannel("A")
    buf = io.BytesIO()
    transparent.convert("RGB").save(buf, format="JPEG", quality=JPEG_QUALITY)
    buf.seek(0)
    compressed_rgb = Image.open(buf).copy()
    compressed_rgb.putalpha(alpha)
    return compressed_rgb


def make_framed_jpeg_variant(transparent: Image.Image, merged: Image.Image) -> Image.Image:
    arr = np.array(merged.convert("RGB"), dtype=np.uint8)
    arr[panel_edge(transparent)] = [0, 0, 0]
    buf = io.BytesIO()
    Image.fromarray(arr).save(buf, format="JPEG", quality=JPEG_QUALITY)
    buf.seek(0)
    return Image.open(buf).copy().convert("RGBA")


def make_gradient_border_variant(transparent: Image.Image, invert: bool = False) -> Image.Image:
    """
    Border pixels filled by a single linear gradient spanning the full page height:
    black→white (invert=False) or white→black (invert=True).
    """
    alpha  = np.array(transparent.getchannel("A"))
    H, W   = alpha.shape
    border = alpha == 0

    t = np.linspace(0.0, 1.0, H, dtype=np.float32)
    if invert:
        t = 1.0 - t
    grad_2d = np.tile((t * 255).astype(np.uint8)[:, np.newaxis], (1, W))

    result = np.array(transparent.copy())
    result[border, 0] = grad_2d[border]
    result[border, 1] = grad_2d[border]
    result[border, 2] = grad_2d[border]
    result[border, 3] = 255
    return Image.fromarray(result)


def make_transparent_framed_jpeg_variant(transparent: Image.Image) -> Image.Image:
    alpha = transparent.getchannel("A")
    arr = np.array(transparent.convert("RGB"), dtype=np.uint8)
    arr[panel_edge(transparent)] = [0, 0, 0]
    buf = io.BytesIO()
    Image.fromarray(arr).save(buf, format="JPEG", quality=JPEG_QUALITY)
    buf.seek(0)
    compressed_rgb = Image.open(buf).copy()
    compressed_rgb.putalpha(alpha)
    return compressed_rgb


VARIANTS = (
    "black",
    "framed", "framed_cleaned", "framed_jpeg", "framed_jpeg_cleaned",
    "gradient_border", "gradient_border_inv",
    "jpeg", "jpeg_cleaned",
)


def process_page(cleaned_path: Path, initial_path: Path, ep_dataset_dir: Path):
    filename = cleaned_path.name
    expected = list(VARIANTS) if initial_path.exists() else [v for v in VARIANTS if v not in ("jpeg", "framed_jpeg")]
    if all((ep_dataset_dir / v / filename).exists() for v in expected):
        return

    cleaned   = Image.open(cleaned_path).convert("RGBA")

    for d in VARIANTS:
        (ep_dataset_dir / d).mkdir(parents=True, exist_ok=True)

    make_black_variant(cleaned).save(ep_dataset_dir / "black" / filename, "PNG")
    make_framed_variant(cleaned).save(ep_dataset_dir / "framed" / filename, "PNG")
    make_transparent_framed_variant(cleaned).save(ep_dataset_dir / "framed_cleaned" / filename, "PNG")
    make_transparent_jpeg_variant(cleaned).save(ep_dataset_dir / "jpeg_cleaned" / filename, "PNG")
    make_transparent_framed_jpeg_variant(cleaned).save(ep_dataset_dir / "framed_jpeg_cleaned" / filename, "PNG")
    make_gradient_border_variant(cleaned, invert=False).save(ep_dataset_dir / "gradient_border" / filename, "PNG")
    make_gradient_border_variant(cleaned, invert=True).save(ep_dataset_dir / "gradient_border_inv" / filename, "PNG")

    if not initial_path.exists():
        print(f"  {filename}: initial render missing — jpeg/framed_jpeg skipped")
        return

    try:
        initial = Image.open(initial_path).convert("RGBA")
    except OSError:
        print(f"  {filename}: initial render corrupt — jpeg/framed_jpeg skipped")
        return
    make_jpeg_variant(initial).save(ep_dataset_dir / "jpeg" / filename, "PNG")
    make_framed_jpeg_variant(cleaned, initial).save(ep_dataset_dir / "framed_jpeg" / filename, "PNG")


def main():
    parser = argparse.ArgumentParser(
        description="Generate dataset variants from preprocessed renders."
    )
    parser.add_argument(
        "episodes",
        nargs="*",
        help="Episode prefix(es) to process (default: ep01). Use 'all' for every episode.",
    )
    args = parser.parse_args()
    targets = args.episodes or ["ep01"]

    if not RENDERS_DIR.exists():
        print(f"Renders directory not found: {RENDERS_DIR}")
        print("Run: python3 src/process_kra.py")
        return

    if targets == ["all"]:
        ep_dirs = sorted(d for d in RENDERS_DIR.iterdir() if d.is_dir())
    else:
        ep_dirs = []
        for t in targets:
            matched = sorted(d for d in RENDERS_DIR.iterdir()
                             if d.is_dir() and d.name.startswith(t))
            if not matched:
                print(f"Warning: no episode matching '{t}' in {RENDERS_DIR}")
            ep_dirs.extend(matched)

    if not ep_dirs:
        print(f"No episode directories found in {RENDERS_DIR}")
        return

    all_pages = []
    for ep_dir in ep_dirs:
        cleaned_dir = ep_dir / "cleaned"
        initial_dir = ep_dir / "initial"
        if not cleaned_dir.exists():
            print(f"Warning: {ep_dir.name} has no cleaned/ — skipping")
            continue
        for png in sorted(cleaned_dir.glob("*.png")):
            all_pages.append((ep_dir, png, initial_dir / png.name))

    bar = tqdm(all_pages, unit="page", desc="synthesize_dataset")
    for ep_dir, cleaned_path, initial_path in bar:
        bar.set_postfix(ep=ep_dir.name.split("_")[0], page=cleaned_path.stem)
        ep_dataset_dir = DATASET_DIR / ep_dir.name
        process_page(cleaned_path, initial_path, ep_dataset_dir)

    print("\nDone.")


if __name__ == "__main__":
    main()
